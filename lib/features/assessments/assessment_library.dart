  /// Category names in stable display order.
  static List<String> get categories => categoryDisplayOrder;

  static List<LibraryAssessment> byCategory(String category) =>
      entries.where((e) => e.category == category).toList();

  static List<LibraryAssessment> search(String query) {
    final q = query.toLowerCase().trim();
    if (q.isEmpty) return List<LibraryAssessment>.from(entries);
    return entries
        .where(
          (e) =>
              e.name.toLowerCase().contains(q) ||
              e.description.toLowerCase().contains(q) ||
              e.category.toLowerCase().contains(q),
        )
        .toList();
  }

  static LibraryAssessment? byId(String id) {
    for (final e in entries) {
      if (e.id == id) return e;
    }
    return null;
  }
