/// Curated assessment reference data (field trial practice, public methodology); bundled with the app, not stored as a database table.
library;

/// Prefix for [TrialAssessments.instructionOverride] machine tags.
const String kCuratedLibraryInstructionPrefix = 'librarySourceId:';

/// Breadcrumb linking a trial assessment row to a curated library entry.
String curatedLibraryInstructionTag(String libraryEntryId) =>
    '$kCuratedLibraryInstructionPrefix$libraryEntryId';

/// Parses [curatedLibraryInstructionTag] ids from trial assessment rows.
Set<String> curatedLibraryIdsFromInstructionOverrides(
  Iterable<String?> instructionOverrides,
) {
  final result = <String>{};
  for (final io in instructionOverrides) {
    final s = io?.trim();
    if (s == null || s.isEmpty) continue;
    if (s.startsWith(kCuratedLibraryInstructionPrefix)) {
      result.add(s.substring(kCuratedLibraryInstructionPrefix.length));
    }
  }
  return result;
}

/// Short unique stored `code` for definitions created from [AssessmentLibrary] entries.
///
/// Schema limits `code` to 50 characters; this format stays well under that (typically
/// under 35) regardless of long curated [libraryEntryId] strings.
String curatedLibraryAssessmentDefinitionCode({
  required int trialId,
  required String libraryEntryId,
  required int disambiguator,
}) {
  final ts = DateTime.now().microsecondsSinceEpoch + disambiguator;
  final h = Object.hash(libraryEntryId, trialId, ts) & 0x7FFFFFFF;
  return 'LIB_${trialId}_$h';
}

